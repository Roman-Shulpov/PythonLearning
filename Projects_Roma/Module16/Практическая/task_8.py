# Задание 8. Симметричная последовательность
# Что нужно сделать
# Последовательность чисел называется симметричной, если она одинаково читается как слева направо, так и
# справа налево. Например, следующие последовательности являются симметричными:
# 1 2 3 4 5 4 3 2 1
# 1 2 1 2 2 1 2 1
# Пользователь вводит последовательность из N чисел. Напишите программу, которая определяет,
# какое минимальное количество и каких чисел нужно добавить в конец этой последовательности, чтобы она стала
# симметричной.
# Пример 1:
# Количество чисел: 5
# Число: 1
# Число: 2
# Число: 1
# Число: 2
# Число: 2
# Последовательность: [1, 2, 1, 2, 2]
# Нужно приписать чисел: 3
# Сами числа: [1, 2, 1]
# Пример 2:
# Количество чисел: 5
# Число: 1
# Число: 2
# Число: 3
# Число: 4
# Число: 5
# Последовательность: [1, 2, 3, 4, 5]
# Нужно приписать чисел: 4
# Сами числа: [4, 3, 2, 1]

n = int(input("Количество чисел: "))
sequence = []

for i in range(n):
    number = int(input("Число: "))
    sequence.append(number)

middle = len(sequence) // 2
symmetric_part = sequence[:middle]

is_symmetric = True
for i in range(middle):
    if sequence[i] != sequence[-(i + 1)]:
        is_symmetric = False
        break

if is_symmetric:
    print("Последовательность уже симметрична")
else:
    numbers_to_add = []
    for i in range(middle - 1, -1, -1):
        numbers_to_add.append(symmetric_part[i])

    if len(sequence) % 2 != 0:
        numbers_to_add.pop(0)

    sequence.extend(numbers_to_add)

    print("Последовательность:", sequence)
    print("Нужно приписать чисел:", len(numbers_to_add))
    print("Сами числа:", numbers_to_add)
